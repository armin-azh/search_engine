def intersect(list1, list2):
    i = 0
    j = 0
    inter = list()
    while i < len(list1) and j < len(list2):
        if list1[i] == list2[j]:
            inter.append(list1[i])
            i += 1
            j += 1
        elif list1[i] < list2[j]:
            i += 1
        else:
            j += 1
    return inter


def union(list1, list2):
    c = list()
    c.extend(list1)
    c.extend(list2)
    c = set(c)
    return sorted(list(c))


def not_operator(list_1, referenced_list):
    ans = list()
    for i in referenced_list:
        if i not in list_1:
            ans.append(i)
    return sorted(ans)


def is_number(token):
    """
    check that if the token is number or not
    :param token:
    :return: (bool)
    """
    try:
        float(token)
        return True
    except ValueError:
        return False


